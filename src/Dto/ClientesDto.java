
package Dto;

public class ClientesDto {
private String nome_clientes, endereco_clientes, email_clientes;
private int id, produto_clientes, valor_clientes, total_clientes;


    public String getNome_clientes() {
        return nome_clientes;
    }

    public void setNome_clientes(String nome_clientes) {
        this.nome_clientes = nome_clientes;
    }

    public String getEndereco_clientes() {
        return endereco_clientes;
    }

    public void setEndereco_clientes(String endereco_clientes) {
        this.endereco_clientes = endereco_clientes;
    }

    public String getEmail_clientes() {
        return email_clientes;
    }

    public void setEmail_clientes(String email_clientes) {
        this.email_clientes = email_clientes;
    }

  
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getProduto_clientes() {
        return produto_clientes;
    }

    public void setProduto_clientes(int produto_clientes) {
        this.produto_clientes = produto_clientes;
    }

    public int getValor_clientes() {
        return valor_clientes;
    }

    public void setValor_clientes(int valor_clientes) {
        this.valor_clientes = valor_clientes;
    }

    public int getTotal_clientes() {
        return total_clientes;
    }

    public void setTotal_clientes(int total_clientes) {
        this.total_clientes = total_clientes;
    }

   

    }

   
   

  
